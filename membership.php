	<h2 class="homesectionheader">Who Are the Knights?</h2>
	<p>
		The Knights of Columbus is the world's largest Catholic family fraternal service organization with 1.7 million members.
		It provides members and their families with volunteer opportunities in service to the Catholic Church, their communities, families and young people.				
	</p>
	<h2 class="homesectionheader">Membership Benefits</h2>
	<p>
		As a member of the Knights of Columbus you and your family enjoy many benefits,
		including 12 free issues annually of Columbia magazine,
		the world's largest Catholic family magazine,
		eligibility to join the Knights of Columbus
		<a href="http://www.kofc.org/un/en/insurance/index.html" target="_blank">top-ranked life insurance program</a>,
		and many more family and personal benefits.				
	</p>
	<p>
		By becoming a member of the St. Michael Council,
		you will be able to support our priest,
		engage in local community service projects,
		and establish a brotherhood with fellow parishioners.
	</p>
	<h2 class="homesectionheader">How to Join</h2>
	<p>
		Membership in the Knights of Columbus is open to practicing Catholic men in union with the Holy See,
		who are at least 18 years old.
		A practicing Catholic is one who lives up to the Commandments of God and the precepts of the Church.
		Application blanks are available from any member of the Knights of Columbus.				
	</p>
	<p>
		If you have any questions or would like more information,
		please <a href="index.php?page=contactus">contact us</a>.				
	</p>
	<h2 class="homesectionheader">4th Degree</h2>
	<p>
		Any Third Degree member in good standing,
		one year after the anniversary of his First Degree,
		is eligible for membership in the Fourth Degree.
		The primary purpose of the Fourth Degree is to foster the spirit of patriotism by promoting responsible citizenship
		and a love of and loyalty to the Knights' respective countries through active membership in local Fourth Degree groups (called "assemblies").
		Certain members of the Fourth Degree serve as honor guards at civic and religious functions,
		an activity that has brought worldwide recognition to the Knights of Columbus.
