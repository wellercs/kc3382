	<h2 class="homesectionheader">About Our Council</h2>
	<p>
		The year was 1950, shortly after the United States entered the Korean Conflict.
		Father John L. Kelly, Pastor of St. Michael Church in Sharonville, Ohio, being impressed by the works of the Knights of Columbus in the
		neighboring areas decided that a Knights of Columbus Council would be beneficial to his parish.  Contact was made with Anthony Bruneman,
		the District Deputy for the Knights in this area and the process was under way.  After several meetings in the parish auditorium enough
		men were interested in Father Kelly's efforts so that a council could be formed.  After the institution degree was held, the council was
		chartered by the Board of Directors of the Knights of Columbus under the direction of Supreme Knight John E. Swift.  Father Leo M. Finn,
		nephew of our Order's founder Father Michael J. McGivney, was the Supreme Chaplain at this time.  The foundinf of our council therefore has
		a direct link to Father McGivney.  Richard Zimmerman became the first Grand Knight of the council and Father Kelly assumed the duties as
		Council Chaplin.				
	</p>
	<p>
		It is well to recognize that over the years, other councils sprung up int he area, notably Blessed Mother Julia Council #5172 in Reading
		and Pope John XXIII Council #7351 in West Chester.  Both these councils had difficulty maintaining their activities and eventually merged
		with St. Michael Council.  We want to commend the former members of these councils who have joined us in active participation in the works
		of the council.				
	</p>
